    private void jButton_EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EditActionPerformed
        // TODO add your handling code here:
          // edit the selected author
        
        String name = jTextField_Name.getText();
        String phoneNumber = jTextField_Phone.getText();
        String address = jTextField_Address.getText();
        
        // check if the textfield is empty
        if(name.trim().isEmpty()) {
            jLabel_EmptyName.setVisible(true);
        } else if(phoneNumber.trim().isEmpty()) {
            jLabel_EmptyPhone.setVisible(true);
        } else if(address.trim().isEmpty()) {
            jLabel_EmptyAddress.setVisible(true);
        }
        else { // if the textfield is not empty
            try 
            {
                int id = Integer.parseInt(jTextField_ID.getText());
                
                SupplierDTO supllierDTO = new SupllierDTO(id, name, phoneNumber, address);
                supplier.upd(supplier);
                
                // populate jtable with author
                populateJtableWithSuppliers();
            } 
            catch (NumberFormatException e) 
            {
                JOptionPane.showMessageDialog(null, "Select the author you want to edit from the table", "Notification", 0);
            } 
        }
    }//GEN-LAST:event_jButton_EditActionPerformed

