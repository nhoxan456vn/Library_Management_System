    private void jButton_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_DeleteActionPerformed
        // TODO add your handling code here:
         try 
            {
                int id = Integer.parseInt(jTextField_ID.getText());
                    
                int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this supplier", "Confirmation Box", JOptionPane.YES_NO_OPTION);
                if(confirm == JOptionPane.YES_OPTION) {
                    supplier.del(id);
                }
                
                // populate jtable with author
                populateJtableWithSuppliers();
                
                // clear text form the textfields
                jTextField_ID.setText("");
                jTextField_Name.setText("");
                jTextField_Phone.setText("");
                jTextField_Address.setText("");
            } 
            catch (NumberFormatException e) 
            {
                JOptionPane.showMessageDialog(null, "Select the supplier you want to delete from the table", "Notification", 0);
            }
    }//GEN-LAST:event_jButton_DeleteActionPerformed

