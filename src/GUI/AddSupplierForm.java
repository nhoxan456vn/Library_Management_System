     public void populateJtableWithSuppliers() {
        ArrayList<SupplierDTO> supplierList = getArrSupplier();
        
        // jtable columns
        String[] colNames = {"ID","NAME", "PHONE", "ADDRESS"};
        
        // jtable rows
        Object[][] rows = new Object[supplierList.size()][colNames.length];
        
        for(int i = 0; i < supplierList.size(); i++) {
            rows[i][0] = supplierList.get(i).getId();
            rows[i][1] = supplierList.get(i).getName();
            rows[i][2] = supplierList.get(i).getPhoneNumber();
            rows[i][3] = supplierList.get(i).getAddress();  
        }
        
        DefaultTableModel model = new DefaultTableModel(rows, colNames);
        jTable_Supplier.setModel(model);
    }
