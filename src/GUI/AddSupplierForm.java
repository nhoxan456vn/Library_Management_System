    private void jButton_AddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_AddMouseClicked
        // TODO add your handling code here:
       
        String name = jTextField_Name.getText();
        String phoneNumber= jTextField_Phone.getText();
        String address = jTextField_Address.getText();
        
        if(name.trim().isEmpty()) {
            jLabel_EmptyName.setVisible(true);
            jTextField_Name.requestFocus();
        } else if(phoneNumber.trim().isEmpty()) {
            jLabel_EmptyPhone.setVisible(true);
            jTextField_Phone.requestFocus();
        } else if(address.trim().isEmpty()) {
            jLabel_EmptyAddress.setVisible(true);
            jTextField_Address.requestFocus();
        }else { // if the textfields are not empty
                SupplierDTO supplier = new SupplierDTO();
                supplier.setName(jTextField_Name.getText());
                supplier.setPhoneNumber(jTextField_Phone.getText());
                supplier.setAddress(jTextField_Address.getText());
                
                // populate jtable with author
                populateJtableWithSuppliers();
             
                jLabel_EmptyName.setVisible(false);
                jLabel_EmptyPhone.setVisible(false);
                jLabel_EmptyAddress.setVisible(false);
        }
    }//GEN-LAST:event_jButton_AddMouseClicked

