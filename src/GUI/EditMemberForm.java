    private void jButton_Edit_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_Edit_ActionPerformed
        // edit the member selected
        
        // get member data
        String firstName = jTextField_FirstName.getText();
        String lastName = jTextField_LastName.getText();
        String phoneNumber = jTextField_PhoneNumber.getText();
        String email = jTextField_Email.getText();
        String gender = jComboBox_Gender.getSelectedItem().toString();
        
        if(firstName.trim().isEmpty()) {
            jLabel_EmptyFirstName_.setVisible(true);
        } else if(lastName.trim().isEmpty()) {
            jLabel_EmptyLastName_.setVisible(true);
        } else if(phoneNumber.trim().isEmpty()) {
            jLabel_EmptyPhoneNumber_.setVisible(true);
        } else {
            // if the textfileds is not empty
            
            String img = null;
            
            if(imagePath != null) {
                int id = Integer.parseInt(jTextField_ID.getText());
                Path path = Paths.get(imagePath);
                String image = String.valueOf(path);
                String picture = image.replace(myPath +"\\", "");
                MemberDTO memberDTO = new MemberDTO(id, firstName, lastName, phoneNumber, email, gender, picture);
                if(member.upd(memberDTO)) {
                    JOptionPane.showMessageDialog(null, "Member edited", "Notification", 1);
                } else {
                    JOptionPane.showMessageDialog(null, "Member not edited", "Notification", 2);
                };   
            } else {
                JOptionPane.showMessageDialog(null, "Select a profile picture for this member", "Notification", 2);
            }
        }
    }//GEN-LAST:event_jButton_Edit_ActionPerformed

