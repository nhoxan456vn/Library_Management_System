    private void jButton_SelectProfilePicture_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SelectProfilePicture_ActionPerformed
        // select picture from the computer
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Select profile picture");
        fileChooser.setCurrentDirectory(new File(myPath));

        FileNameExtensionFilter extensionFilter = new FileNameExtensionFilter("Image", ".png", ".jpg", ".jpeg");
        fileChooser.addChoosableFileFilter(extensionFilter);

        int fileState = fileChooser.showSaveDialog(null);

        if(fileState == JFileChooser.APPROVE_OPTION) {
            String path = fileChooser.getSelectedFile().getAbsolutePath();
            jLabel_ImagePath.setText(path);
            imagePath = path;
            
            // display the image
            func.displayImageByPath(jLabel_Image.getWidth(), jLabel_Image.getHeight(), imagePath, jLabel_Image);
        }
    }//GEN-LAST:event_jButton_SelectProfilePicture_ActionPerformed

    private void jButton_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SearchActionPerformed
   
        try {
            
            // search member by ID and display data
            int id = Integer.parseInt(jTextField_ID.getText());
            // get member data
            MemberDTO selectedMember = member.getMemberByID(id);
            
            if(selectedMember != null) {
                jTextField_ID.setText(selectedMember.getId()+"");
                jTextField_FirstName.setText(selectedMember.getFirstName());
                jTextField_LastName.setText(selectedMember.getLastName());
                jTextField_PhoneNumber.setText(selectedMember.getPhoneNumber());
                jTextField_Email.setText(selectedMember.getEmail());
                jComboBox_Gender.setSelectedItem(selectedMember.getGender());
            
            // display the member image
            String image = selectedMember.getPicture();
            String picture = myPath + "\\" + image;
            
            func.displayImageByPath(jLabel_Image.getWidth(), jLabel_Image.getHeight(), picture, jLabel_Image);
            } else {
                JOptionPane.showMessageDialog(null, "No member with this id is found", "Notification", 3);
            }
            
        } catch (NumberFormatException ex) {
            //Logger.getLogger(EditMemberForm.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Enter the Valid Member ID", "Notification", 3);
        }
        
        
    }//GEN-LAST:event_jButton_SearchActionPerformed

