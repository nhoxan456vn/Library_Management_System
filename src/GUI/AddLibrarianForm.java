    private void jButton_AddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_AddMouseClicked
        // TODO add your handling code here:
        String firstName = jTextField_FirstName.getText();
        String lastName = jTextField_LastName.getText();
        
        String phoneNumber = jTextField_Phone.getText();
        String address = jTextField_Address.getText();
        String email = jTextField_Email.getText();
        String gender = jComboBox_Gender.getSelectedItem().toString();
        
        
        
        if(firstName.trim().isEmpty()) {
            jLabel_EmptyFirstName.setVisible(true);
        } else if(lastName.trim().isEmpty()) {
            jLabel_EmptyLastName.setVisible(true);
        } else if(phoneNumber.trim().isEmpty()) {
            jLabel_EmptyPhone.setVisible(true);
        }
        else { // if the textfields are not empty
            String image = null;
            if(imagePath != null) {
               Path path = Paths.get(imagePath);
                image = String.valueOf(path);
               
                String picture = image.replace(myPath + "\\", "");
                LibrarianDTO librarianDTO = new LibrarianDTO(firstName, lastName, phoneNumber, email, gender, address, picture);
                if(librarian.add(librarianDTO)) {
                    JOptionPane.showMessageDialog(null, "MLibrarian added", "Notification", 1);
                } else {
                    JOptionPane.showMessageDialog(null, "Librarian not added", "Notification", 2);
                };
            } else {
                JOptionPane.showMessageDialog(null, "Select a profile picture for this member", "Notification", 2);
            }
        }
    }//GEN-LAST:event_jButton_AddMouseClicked

