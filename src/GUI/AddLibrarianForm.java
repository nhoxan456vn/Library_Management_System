    public void populateJtableWithLibrarian() {
        ArrayList<LibrarianDTO> librarianList = librarian.getArrLibrarian();
        
        // jtable columns
        String[] colNames = {"FIRST NAME", "LAST NAME", "YEAROFBIRTH", "PHONE", "ADDRESS", "EMAIL"};
        
        // jtable rows
        Object[][] rows = new Object[librarianList.size()][colNames.length];
        
        for(int i = 0; i < librarianList.size(); i++) {
            rows[i][0] = librarianList.get(i).getId();
            rows[i][1] = librarianList.get(i).getFirstName();
            rows[i][2] = librarianList.get(i).getLastName();
            rows[i][3] = librarianList.get(i).getPhoneNumber();
            rows[i][4] = librarianList.get(i).getAddress();
            rows[i][5] = librarianList.get(i).getEmail();
        }
        
        DefaultTableModel model = new DefaultTableModel(rows, colNames);
        jTable_Librarian_.setModel(model);
    }
